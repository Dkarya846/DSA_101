public class Program {
    public static void main(String[] args) {
        System.out.println("Bitwise Operator in Java: ");
        System.out.println("AND: 5&10: "+(5&10));
        System.out.println("OR: 5|10: "+(5|10));
        System.out.println("NOT ~5: "+~5);
        System.out.println("Left Shift: 3<<1: "+ (3 << 4));     
    }
}
